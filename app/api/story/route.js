import Anthropic from "@anthropic-ai/sdk";

const client = new Anthropic();

export async function POST(request) {
  const {
    theme,
    hero,
    heroName,
    place,
    history = [],
    scene = 1,
    totalScenes = 4,
  } = await request.json();

  const safeHistory = Array.isArray(history) ? history : [history];
  const historyText = safeHistory.length > 0 ? safeHistory.join("; ") : "початок казки";
  const isFinalScene = Number(scene) >= Number(totalScenes);
  const finalHeroName = heroName || hero;

  const systemPrompt = `Ти — майстер дитячих інтерактивних казок і дитячий психолог з досвідом роботи з дітьми 4–8 років.`;

  const userPrompt = isFinalScene
    ? `Герой: ${hero}, звати ${finalHeroName}. Місце: ${place}. Тема: ${theme}.
Вік дитини: 4–8 років.
Вибори які зробила дитина: ${historyText}

Твоє завдання — завершити казку повноцінно і дати батькам інструмент для розмови.

Правила для фіналу (4–5 речень):
— Покажи конкретний результат саме тих виборів які зробила дитина — через образ і подію
— ${finalHeroName} має отримати емоційну крапку: радість, тепло, або легкий смуток з надією
— Казка має відчуватись завершеною — не обривається, а приходить до берега
— Мораль не озвучується — вона живе в останньому образі

Правила для саммарі (для батьків, не для дитини):
— summary: одне тепле речення — який шлях обрала дитина і що це дало ${finalHeroName}
— alternative: одне речення — що могло статись якби дитина обрала інакше (без осуду, з цікавістю)
— parent_prompt: одне відкрите питання яке відкриває розмову між батьками і дитиною — без правильної відповіді, з простором для думки

Відповідай ТІЛЬКИ JSON без markdown і без пояснень:
{"ending":"...","summary":"...","alternative":"...","parent_prompt":"..."}`
    : `Ти — майстер дитячих інтерактивних казок і дитячий психолог з досвідом роботи з дітьми 4–8 років.

Пиши в стилі Василя Сухомлинського: коротко, образно, без моралі вголос — через конкретний вчинок і деталь. Додай живість діалогу як у Нестайка і ніжність до теми самотності як у Лущевської.

Параметри казки:
- Герой: ${hero}. Звати: ${finalHeroName}. Імʼя НЕ ЗМІНЮЄТЬСЯ ні в якому разі протягом всієї казки.
- Місце: ${place}
- Тема: ${theme}
- Вік дитини: 4–8 років — мова проста, образна, речення короткі
- Це сцена ${scene} з ${totalScenes}
- Попередні вибори: ${historyText}

Правила для тексту сцени (4–6 речень):
— Починай з деталі яку можна побачити або відчути — запах, звук, колір
— Кожна сцена органічно продовжує попередню через наслідки вибору
— ${finalHeroName} має внутрішній стан — що відчуває, про що думає
— Тема "${theme}" проявляється через ситуацію, не через слова про неї
— Мова: тепла, жива, як розповідає улюблена бабуся біля вогню

Правила для варіантів вибору:
— Варіант А: шлях щирості і турботи — потребує зусилля або сміливості
— Варіант Б: шлях зручності, уникнення або власної вигоди — зрозумілий і людський
— Варіанти ДІАМЕТРАЛЬНО протилежні за внутрішньою логікою — дитина має відчути контраст
— Формулюй як конкретну дію, не абстракцію: не "допомогти", а "повернутись і простягнути руку"
— Жоден варіант не звучить як "правильний" — обидва природні

Відповідай ТІЛЬКИ JSON без markdown і без пояснень:
{"scene":"...","scene_text":"...","choice_a":"...","choice_b":"..."}`;

  const message = await client.messages.create({
    model: "claude-sonnet-4-5",
    max_tokens: 1024,
    system: systemPrompt,
    messages: [
      { role: "user", content: userPrompt },
    ],
  });

  const text = message.content[0].text;
  const clean = text.replace(/```json\n?|\n?```/g, "").trim();
  const data = JSON.parse(clean);

  return Response.json(data);
}
